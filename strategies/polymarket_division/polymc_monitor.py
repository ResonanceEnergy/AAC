"""
PolyMC Monitor — Hourly Monitoring Loop with Exit Strategy Engine
==================================================================
AAC Polymarket Division

Monitors all portfolio positions hourly:
  - Refreshes live prices from CLOB/Gamma API
  - Checks per-bet exit signals (take-profit / stop-loss)
  - Checks portfolio-level exit rules (EV floor, partial profit)
  - Logs all signals and state transitions
  - Can auto-execute exits via CLOB SDK (sell limit orders)

Exit Strategy Rules:
  Per-Bet:
    - Take Profit: price >= bet.take_profit --> SELL
    - Stop Loss:   price <= bet.stop_loss   --> SELL
    - Partial Profit: price >= entry * 1.5  --> SELL 50%

  Portfolio-Level:
    - EV Floor: portfolio EV < 4% --> exit all positions
    - Concentration: single bet > 40% of portfolio value --> rebalance alert

Usage:
    python -m strategies.polymarket_division.polymc_monitor             # one check
    python -m strategies.polymarket_division.polymc_monitor --loop      # hourly loop
    python -m strategies.polymarket_division.polymc_monitor --json      # JSON output
    python -m strategies.polymarket_division.polymc_monitor --execute   # auto-execute exits
"""
