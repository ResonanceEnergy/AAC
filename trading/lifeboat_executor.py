"""
Storm Lifeboat Executor — Commodity ETF + Moon Phase Bridge
=============================================================
Maps Storm Lifeboat SeeSawSignal objects → exchange orders via
StrategicDoctrineEngine terrain assessment and lunar timing gates.

The Storm Lifeboat produces SeeSawSignal and TrackedPosition objects
but has no execution path.  This module closes that gap:

    1. Reads current SeeSaw phase, signals, and tracked positions
    2. Checks lunar timing window (golden-ratio offsets within 28-day moons)
    3. Calls StrategicDoctrineEngine.assess_terrain() for go/no-go
    4. Generates ETF/commodity orders routed via IBKR or Moomoo
    5. Manages entry/exit with stop-loss from TrackedPosition

Usage:
    executor = LifeboatExecutor()
    await executor.connect()
    orders = executor.generate_orders(cycle_report, market_snapshot)
    if not executor.dry_run:
        results = await executor.execute(orders)
"""
